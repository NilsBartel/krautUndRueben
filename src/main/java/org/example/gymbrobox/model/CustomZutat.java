package org.example.gymbrobox.model;


public class CustomZutat {

    private String name;
    private int menge;

    public CustomZutat() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMenge() {
        return menge;
    }

    public void setMenge(int menge) {
        this.menge = menge;
    }
}
